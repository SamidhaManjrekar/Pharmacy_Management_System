    private void checkUsername(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_checkUsername
        // Tick or Cross to show if the username is taken or not and also checks if it is taken
        check = 0;
        String userName = jTextField2.getText();
        if (!userName.isEmpty()) {
            try {
                Connection con = ConnectionProvider.getCon();
                Statement stmt = con.createStatement();
                ResultSet rs = stmt.executeQuery("SELECT * FROM USER WHERE username = '" + userName + "'");
                if (rs.next()) {
                    check = 1;
                    jLabel11.setVisible(true);
                    jLabel11.setIcon(new ImageIcon("/Users/samidhamanjrekar/NetBeansProjects/PharmacyManagementSystem/src/Images/cross.png"));
                    jLabel11.setText("");
                } else {
                    jLabel11.setVisible(true);
                    jLabel11.setIcon(new ImageIcon("/Users/samidhamanjrekar/NetBeansProjects/PharmacyManagementSystem/src/Images/tick.png"));
                    jLabel11.setText("");
                }
                con.close();
                stmt.close();
                rs.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            jLabel11.setVisible(false);
        }
    }//GEN-LAST:event_checkUsername

