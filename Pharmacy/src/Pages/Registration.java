    private void submitButton(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButton
        // Submit button to add user details into the dateabase

        String name = jTextField1.getText();
        String userName = jTextField2.getText();
        String password = new String(jPasswordField1.getPassword());
        String confirmPassword = new String(jPasswordField2.getPassword());
        String address = jTextArea1.getText();
        String phone = jTextField5.getText();
        String email = jTextField6.getText();
        String dateOfBirth = "";
        Date selectedDate = jDateChooser1.getDate();
        Date currentDate = new Date();

        if (selectedDate != null) {
            if (selectedDate.after(currentDate)) {
                JOptionPane.showMessageDialog(this, "Invalid Date Of Birth!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
            dateOfBirth = dFormat.format(selectedDate);
        }

        if (name.isEmpty() || userName.isEmpty() || password.isEmpty() || confirmPassword.isEmpty() || address.isEmpty() || phone.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields are required!", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Password does not match the confirm password!", "Error", JOptionPane.ERROR_MESSAGE);
            jPasswordField1.setText("");
            jPasswordField2.setText("");
        } else if (phone.length() != 10 || !phone.matches(phonePattern)) {
            JOptionPane.showMessageDialog(this, "Invalid phone number!", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField5.setText("");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                String sql = "INSERT INTO USER (name, username, password, dateOfBirth, address, phone, email) "
                        + "VALUES (?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement stmt = con.prepareStatement(sql);
                stmt.setString(1, name);
                stmt.setString(2, userName);
                stmt.setString(3, password);
                stmt.setString(4, dateOfBirth);
                stmt.setString(5, address);
                stmt.setString(6, phone);
                stmt.setString(7, email);

                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Account created successfully!");
                setVisible(false);
                new PharmacistDashboard(userName, name).setVisible(true);

                con.close();
                stmt.close();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error occurred while registering user: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_submitButton

