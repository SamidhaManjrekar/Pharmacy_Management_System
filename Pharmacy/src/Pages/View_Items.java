    private void showTable(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_showTable
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        try {
            Connection con = ConnectionProvider.getCon();
            String sql = "select * from medicine";
            PreparedStatement stmt = con.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();
            model.setRowCount(0);
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("name"),
                    rs.getString("type"),
                    rs.getString("price"),
                    rs.getString("stock"),
                    rs.getString("manufacturer")
                });
            }
            con.close();
            rs.close();
            stmt.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_showTable

    private void searchView(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchView
        //To search for user in view item page

        String search = jTextField18.getText();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        try {
            Connection con = ConnectionProvider.getCon();
            String sql = "SELECT * FROM MEDICINE WHERE Name LIKE ?";
            PreparedStatement stmt = con.prepareStatement(sql);

            if (search.isEmpty()) {
                sql = "SELECT * FROM MEDICINE";
                stmt = con.prepareStatement(sql);
            } else {
                sql = "SELECT * FROM MEDICINE WHERE Name LIKE ?";
                stmt = con.prepareStatement(sql);
                stmt.setString(1, "%" + search + "%");
            }

            ResultSet rs = stmt.executeQuery();

            model.setRowCount(0);
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("name"),
                    rs.getString("type"),
                    rs.getString("price"),
                    rs.getString("stock"),
                    rs.getString("manufacturer")
                });
            }
            con.close();
            rs.close();
            stmt.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_searchView

