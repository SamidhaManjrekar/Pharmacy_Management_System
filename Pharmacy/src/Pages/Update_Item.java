    private void jLabel38searchUpdate(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel38searchUpdate
        //To search for items in Update item page
        String search = jTextField17.getText();

        if (search.isEmpty()) {
            jTextField4.setText("");
            jTextField13.setText("");
            jTextField12.setText("");
            jTextField11.setText("");
            jComboBox2.removeAllItems();
            jComboBox2.addItem("Medicine");
            jComboBox2.addItem("Medical Tool");
            return;
        }
        try {
            Connection con = ConnectionProvider.getCon();
            String sql = "SELECT * FROM medicine WHERE name = ?";
            PreparedStatement stmt = con.prepareStatement(sql);
            stmt.setString(1, search);
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                jTextField13.setText(rs.getString("name"));
                jTextField13.setEditable(false);
                jTextField4.setText(rs.getString("manufacturer"));
                jTextField12.setText(rs.getString("stock"));
                jTextField11.setText(rs.getString("price"));

                String type = rs.getString("Type");
                jComboBox2.removeAllItems();
                if (type.equals("Medicine")) {
                    jComboBox2.addItem("Medicine");
                    jComboBox2.addItem("Medical Tool");
                } else {
                    jComboBox2.addItem("Medical Tool");
                    jComboBox2.addItem("Medicine");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Item does not exist!");
            }
            con.close();
            rs.close();
            stmt.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabel38searchUpdate

