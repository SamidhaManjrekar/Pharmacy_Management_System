    private void jButton4updateButton(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4updateButton
        String name = jTextField13.getText();
        String price = jTextField11.getText();
        String stock = jTextField12.getText();
        String type = (String) jComboBox2.getSelectedItem();
        String manufacturer = jTextField4.getText();

        if (name.equals("") || price.equals("") || stock.equals("") || type.equals("") || manufacturer.equals("")) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else if (!price.matches(numberPattern) || !stock.matches(numberPattern)) { // Check both price and stock for numeric values
            JOptionPane.showMessageDialog(null, "Price and Stock should be positive integers");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update medicine set manufacturer=?, type=?, price=?, stock=? where name=?");
                ps.setString(1, manufacturer);
                ps.setString(2, type);
                ps.setString(3, price);
                ps.setString(4, stock); 
                ps.setString(5, name);

                int rowsUpdated = ps.executeUpdate();
                if (rowsUpdated > 0) {
                    JOptionPane.showMessageDialog(null, "Medicine updated successfully");
                    setVisible(false);
                    new Update_Item().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to update medicine");
                }
                ps.close();
                con.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton4updateButton

