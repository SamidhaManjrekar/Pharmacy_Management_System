    private void checkTaken(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_checkTaken
        // TODO add your handling code here:
        check = 0;
        String userName = jTextField7.getText();
        if (!userName.isEmpty()) {
            try {
                Connection con = ConnectionProvider.getCon();
                Statement stmt = con.createStatement();
                ResultSet rs = stmt.executeQuery("SELECT * FROM USER WHERE username = '" + userName + "'");
                if (rs.next()) {
                    check = 1;
                } else {
                    check = 0;
                }
                con.close();
                stmt.close();
                rs.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "An error occurred while checking username.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_checkTaken

