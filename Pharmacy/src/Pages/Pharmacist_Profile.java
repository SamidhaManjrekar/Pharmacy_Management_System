    private void jPanel1ComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_jPanel1ComponentShown
       try {
            Connection con = ConnectionProvider.getCon();
            String sql = "SELECT * FROM USER WHERE name = ?";
            PreparedStatement stmt = con.prepareStatement(sql);
            stmt.setString(1, name);
            ResultSet rs = stmt.executeQuery();
            while (rs.next()) {
                jTextField6.setText(rs.getString("name"));
                jTextField7.setText(rs.getString("UserName"));
                jTextField8.setText(rs.getString("Password"));
                jTextField9.setText(rs.getString("Phone"));
                jTextField10.setText(rs.getString("Email"));
                jTextArea2.setText(rs.getString("Address"));
                SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
                String dateOfBirth = rs.getString("DateOfBirth");
                if (dateOfBirth != "") {
                    try {
                        jDateChooser2.setDate(dFormat.parse(dateOfBirth));
                    } catch (ParseException e) {
                        JOptionPane.showMessageDialog(this, "Error parsing date of birth: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
            con.close();
            rs.close();
            stmt.close();

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jPanel1ComponentShown

