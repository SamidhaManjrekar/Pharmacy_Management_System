    private void updateProfile(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateProfile
        // To update the Pharmacist information

        String name = jTextField6.getText();
        String userName = jTextField7.getText();
        String password = jTextField8.getText();
        String email = jTextField10.getText();
        String phone = jTextField9.getText();
        String address = jTextArea2.getText();
        String dateOfBirth = "";
        Date selectedDate = jDateChooser2.getDate();
        Date currentDate = new Date();

        if (selectedDate != null) {
            if (selectedDate.after(currentDate)) {
                JOptionPane.showMessageDialog(this, "Invalid Date Of Birth!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
            dateOfBirth = dFormat.format(selectedDate);
        }

        if (name.isEmpty() || userName.isEmpty() || password.isEmpty() || address.isEmpty() || phone.isEmpty() || email.isEmpty() || dateOfBirth.isEmpty()) {
            JOptionPane.showMessageDialog(null, "All fields are required!", "Input Error", JOptionPane.ERROR_MESSAGE);
        } else if (phone.length() != 10 || !phone.matches(phonePattern)) {
            JOptionPane.showMessageDialog(this, "Invalid phone number!", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField9.setText("");
        } else if (check == 1) {
            JOptionPane.showMessageDialog(this, "User Name Taken!", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField7.setText("");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                String sql = "UPDATE USER SET name = ?, UserName = ?, Password = ?, DateOfBirth = ?, address = ?, phone = ?, email = ? WHERE name = ?";
                PreparedStatement stmt = con.prepareStatement(sql);
                stmt.setString(1, name);
                stmt.setString(2, userName);
                stmt.setString(3, password);
                stmt.setString(4, dateOfBirth);
                stmt.setString(5, address);
                stmt.setString(6, phone);
                stmt.setString(7, email);
                stmt.setString(8, name);

                stmt.executeUpdate();

                JOptionPane.showMessageDialog(null, "Account updated successfully!");

                con.close();
                stmt.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error occurred while updating user account: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_updateProfile

